    CheckPhoneCode(countryPhoneCode) {

        let isValid = true;

        if (countryPhoneCode !== "") {

            const validChars = "+0123456789";
            const codeArray = Array.from(countryPhoneCode);


            codeArray.forEach(character => {

                if (!validChars.includes(character)) {

                    isValid = false;
                }

            });


            if (isValid) {

                const result = this.countries.filter(code => code["CallingCode"] === countryPhoneCode);

                if (result.length > 0) {

                    isValid = true;

                } else {

                    isValid = false;

                }



            }

        }


        return isValid;

    }
 
